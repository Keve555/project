    <h1>Opel</h1>
    <h5>Ismertebb modellei, rekordjai és történelme.</h5>
    <p>Az Adam Opel által Rüsselsheimben, 1862. január 21-én alapított családi vállalkozás eleinte varrógépeket, később kerékpárokat gyártott. Autógyártással a családi cég Adam Opel 1895-ben bekövetkezett halálig semmilyen vonatkozásban nem foglalkozott.</p>
    <p>Adam Opel halála után cégét özvegye és két fia vette át. Folytatták a kerékpárgyártást, majd 1899-ben bekapcsolódtak az autógyártásba.</p>
    <p>Rövid időn belül az Opel az egyik legnagyobb német autógyártóvá fejlődött. Ebben szerepet játszott az is, hogy a rüsselheimi telephelyen 1924-ben bevezetett futószalagnak köszönhetően a cég olcsó modelleket állított elő, amelyek iránt a kereslet is rohamosan megnőtt.</p>
